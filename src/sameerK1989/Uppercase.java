package sameerK1989;

public class Uppercase {

	static void Uppercase(String strg)
	{
		System.out.println("Upper case string is " + strg.toUpperCase());
		System.out.println("Lower case string is " + strg.toLowerCase());
	}
	public static void main(String[] args) {
		Uppercase ("sAmEeR kAMbLe");
	}
}
