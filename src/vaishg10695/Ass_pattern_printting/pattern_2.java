package vaishg10695.Ass_pattern_printting;

public class pattern_2 {

	public static void main(String[] args) {

		int n = 5;
		for (int i = 1; i <= n; i++) {
			for (int j = 1;j <= i; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
	}

}
